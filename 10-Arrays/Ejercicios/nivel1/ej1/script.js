// Creación del arreglo vacío para la lista de supermercado
let listaDeSuper = [];

// Agregar productos a la lista de supermercado
listaDeSuper.push("sal");
listaDeSuper.push("azúcar");
listaDeSuper.push("pan");
listaDeSuper.push("leche");
listaDeSuper.push("huevos");

// Acceder al primer elemento de la lista
console.log("Primer elemento de la lista: " + listaDeSuper[0]);

// Crear una variable para almacenar la posición del último elemento
let ultimoElemento = listaDeSuper.length - 1;

// Acceder al último elemento de la lista usando la variable ultimoElemento
console.log("Último elemento de la lista: " + listaDeSuper[ultimoElemento]);